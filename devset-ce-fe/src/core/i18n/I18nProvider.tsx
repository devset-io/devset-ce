const resolveDefaultLocale = (): Locale => {
  if (typeof window === 'undefined') {
    return 'en'
  }
  const saved = window.localStorage.getItem(LOCALE_STORAGE_KEY)
  if (saved === 'pl' || saved === 'en') {
    return saved
  }
  return 'en'
}

const interpolate = (template: string, params?: TranslateParams) => {
  if (!params) {
    return template
  }
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? `{${key}}`))
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocale] = useState<Locale>(resolveDefaultLocale)

  useEffect(() => {
    window.localStorage.setItem(LOCALE_STORAGE_KEY, locale)
    document.documentElement.lang = locale
  }, [locale])

  const t = useCallback(
    (key: string, params?: TranslateParams) => {
      const dictionary = TRANSLATIONS[locale]
      const fallbackDictionary = TRANSLATIONS.en
      const template = dictionary[key] ?? fallbackDictionary[key] ?? key
      return interpolate(template, params)
    },
    [locale],
  )

  const value = useMemo<I18nContextValue>(
    () => ({
      locale,
      setLocale,
      t,
    }),
    [locale, t],
  )

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

/** Translate a key for a given locale, outside React context. */
// eslint-disable-next-line react-refresh/only-export-components
export function translateFor(locale: Locale, key: string): string {
  return TRANSLATIONS[locale][key] ?? TRANSLATIONS.en[key] ?? key
}

// eslint-disable-next-line react-refresh/only-export-components
export const useI18n = () => {
  const context = useContext(I18nContext)
  if (!context) {
    throw new Error('useI18n must be used within I18nProvider')
  }
  return context
}
